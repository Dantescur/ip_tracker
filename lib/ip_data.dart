class IpData {
  final String ip;
  final String city;
  final String region;
  final String timezone;
  final String isp;
  final String postal;
  final double lat;
  final double lng;

  IpData({
    required this.ip,
    required this.city,
    required this.region,
    required this.timezone,
    required this.isp,
    required this.postal,
    required this.lat,
    required this.lng,
  });
}
